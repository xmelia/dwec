/* FUNCIO DE SUMAR */
function suma(a,b){
    return a+b;
}

/* FUNCIO DE RESTAR */
function resta(a,b){
    return (a-b);                
}

 /* FUNCIO DE MULTIPLICAR */
 function multi(a,b){
    return (a*b);                
}

 /* FUNCIO DE DIVIDIR */
 function div(a,b){
    return (a/b);                
}

